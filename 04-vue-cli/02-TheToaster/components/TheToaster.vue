<template>
  <div class="toasts">
    <div class="toast__list">
      <template v-for="toast in toastList" :key="toast.id" @remove="removeToast">
        <UiToast :toast="toast" />
      </template>
    </div>
  </div>
</template>

<script>
import UiIcon from './UiIcon';
import UiToast from './UiToast.vue';

export default {
  name: 'TheToaster',

  components: {
    UiIcon,
    UiToast,
  },

  data() {
    return {
      toastList: [],
    };
  },

  methods: {
    success(msg) {
      this.toastList.push({
        type: 'success',
        msg,
        isVisible: true,
      });

      this.removeToast();
    },

    error(msg) {
      this.toastList.push({
        type: 'error',
        msg,
        isVisible: true,
      });

      this.removeToast();
    },

    removeToast() {
      setTimeout(() => {
        this.toastList.splice(0, 1);
      }, 5000);
    },
  },
};
</script>

